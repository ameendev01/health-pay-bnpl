
"use client";

import { useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";
import { z } from "zod";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Form, FormControl, FormField, FormItem, FormLabel, FormMessage } from "@/components/ui/form";
import { setUpBankSchema } from "./schemas";

interface SetUpBankFormProps {
  onSuccess: () => void;
}

export default function SetUpBankForm({ onSuccess }: SetUpBankFormProps) {
  const form = useForm<z.infer<typeof setUpBankSchema>>({
    resolver: zodResolver(setUpBankSchema),
    defaultValues: {
      bankName: "",
      accountNumber: "",
    },
  });

  function onSubmit(values: z.infer<typeof setUpBankSchema>) {
    console.log(values);
    // Here you would typically send data to your backend
    onSuccess();
  }

  return (
    <Form {...form}>
      <form onSubmit={form.handleSubmit(onSubmit)} className="space-y-4">
        <FormField
          control={form.control}
          name="bankName"
          render={({ field }) => (
            <FormItem>
              <FormLabel>Bank Name</FormLabel>
              <FormControl>
                <Input placeholder="Bank of America" {...field} />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />
        <FormField
          control={form.control}
          name="accountNumber"
          render={({ field }) => (
            <FormItem>
              <FormLabel>Account Number</FormLabel>
              <FormControl>
                <Input placeholder="123456789" {...field} />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />
        <Button type="submit">Submit</Button>
      </form>
    </Form>
  );
}
